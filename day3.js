function sliceIntoChunks(arr) {
    const res = [];
    for (let i = 0; i < arr.length; i += 3) {
        const chunk = arr.slice(i, i + 3);
        res.push(chunk);
    }
    return res;
}

const elvesGroups = sliceIntoChunks(splitInput);

const findBadge = (rucksackGroup) => {
    const firstPart = rucksackGroup[0].split('');
    const secondPart = rucksackGroup[1];
    const thirdPart = rucksackGroup[2];
    const compare = firstPart.find((e) => secondPart.includes(e) && thirdPart.includes(e));
    return alphabet.indexOf(compare) + 1;
}

