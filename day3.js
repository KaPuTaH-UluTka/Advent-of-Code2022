const checkRucksack = (rucksack) => {
    rucksack = rucksack.split('');
    const firstPart = rucksack.splice(0, rucksack.length / 2);
    const secondPart = rucksack;
    const compare = firstPart.find((e) => secondPart.includes(e));
    return alphabet.indexOf(compare) + 1;
}

